package com.java.bean;

public class InnerOuterBean {

	String innerChar;
	String outerChar;
	
	public String getInnerChar() {
		return innerChar;
	}
	
	public void setInnerChar(String innerChar) {
		this.innerChar = innerChar;
	}
	public String getOuterChar() {
		return outerChar;
	}
	public void setOuterChar(String outerChar) {
		this.outerChar = outerChar;
	}
	
}
